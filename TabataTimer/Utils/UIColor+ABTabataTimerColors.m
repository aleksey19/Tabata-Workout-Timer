#import "UIColor+ABTabataTimerColors.h"

@implementation UIColor (ABTabataTimerColors)

+ (UIColor *)aquaColor {
    return [UIColor colorWithRed:0 / 255.f green:128 / 255.f blue:255 / 255.f alpha:1.0];
}

+ (UIColor *)brightRedColor {
    return [UIColor colorWithRed:232 / 255.f green:0 / 255.f blue:0 / 255.f alpha:1.0];
}

+ (UIColor *)softGreenColor {
    return [UIColor colorWithRed:0 / 255.f green:128 / 255.f blue:0 / 255.f alpha:1.0];
}

@end
